public class User{
    String id;
    String name;
    

    public String getId() {
        return id;
    }
    public String getName() {
        return name;
    }
    public User(String id, String name) {
        this.id = id;
        this.name = name;
    }
    public void setName(String name) {
        this.name = name;
    }
    
    public User() {
    }
    


}